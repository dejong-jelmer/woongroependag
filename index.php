          <li>Door banden en contacten aan te halen bouwen we aan gemeenschap groter dan onze eigen woongroep.</li>
          <li>Wonen in woongroepen en ontwikkeling van nieuwe woongemeenschappen staan onder druk van de veranderende huurwetgeving. Samen staan we sterker.</li>
