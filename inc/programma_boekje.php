<h1>Programma boekje</h1>

<h3>Blok I Ecologiseren</h3> <br />
<h4>Spreker: <i>Sissy Verspeek</i></h4> <br />

<b>Workshop: Ecologiseren van bestaande panden</b> <br />

<i>Workshopbegeleider: Sissy Verspeek</i> <br />
<p>Er wordt een korte inleiding gegeven door Sissy Verspeek over ecologiseren van een bestaand pand, biobased materialen en natuurlijke isolatie.</p> <br />

<p>Heb je oplossingen voor hoe je oude panden beter kan isoleren. Kom ze delen. Hoe kan je enkel-glas beter isoleren.</p> <br />

<b>Workshop: Ecologische nieuwbouw.</b> <br />
<i>Workshopbegeleider: Mare Nynke, Iewan</i> <br />

<p>Je wilt een nieuwe woongemeenschap oprichten en ecologisch bouwen. Hoe pak je dit aan. Iewan legt uit hoe zij dit hebben gedaan.</p> <br />

<b>Workshop: Eco inrichten en afwerken in je huis.</b> <br />
<i>Workshopbegeleider: Inti</i> <br />
<p>Plafond witten, een nieuwe vloer in je woning. Welke eco materialen zijn er eigenlijk. Deel jou eco-klusmanieren. Er worden voorbeelden gegeven die bij Iewan zijn gebruikt.</p> <br />

<b>Workshop: Gemeenschappelijk (zonne)energie opwekken.</b> <br />
<i>Workshopbegeleider: Jelmer, Iewan</i> <br />
<p>Je wilt zonnepanelen met je pand aanschaffen maar hoe doe je dit? Welke contracten kan je het beste afsluiten? Iewan deelt.</p> <br />
 
<h3>BLOK II: Woongemeenschappen in de toekomst</h3> <br />
<h4>Spreker: Yvonne Witter</h4> <br />
<p>Yvonne Witter is adviseur bij het Aedes-Actiz Kenniscentrum Wonen-Zorg. Zij houdt zich daar o.a. bezig met woonvariaties. Yvonne zal een inleiding geven over het belang van woongemeenschappen nu en in de toekomst. Zorg voor elkaar, langer zelfstandig wonen, gebruik maken van de mogelijkheden die mensen zelf in huis hebben. Aan de hand van tal van voorbeelden van verschillende woonvormen legt ze uit hoe je door samenwerking je doelen kunt bereiken.
</p> <br />
<b>Visie op de volkshuisvesting in de komende jaren.</b> <br />
<i>Workshopbegeleider: Bernard - WBVG</i> <br />
<p>De (sociale) huisvesting in Nederland is sterk in beweging. Bestaan woningcorporaties over 5 jaar nog? Moeten we het noodgedwongen allemaal zelf gaan regelen? En worden we daarin juist niet tegengewerkt door alle nieuwe regeltjes?</p> <br />

<b>Cooplink, waarom en hoe van het kennisnetwerk wooncoöperaties.</b> <br />
<i>Workshopbegeleider: Agnes Verweij, Woonbond</i> <br />
<p>De nieuwe woning wet  geeft de mogelijkheid dat woongroepen zelf een wooncoöperatie oprichten. In de workshop wordt verteld welke mogelijkheden er zijn, waar kennis vandaan haalt en hoe je aan de slag kan gaan. Cooplink helpt, het nieuwe kennisnetwerk voor wooncoöperaties.</p> <br />

<b>Hoe financier je je woongemeenschap</b> <br />
<i>Workshopbegeleider: Jan en Julia, de Groene Gemeenschap</i> <br />
<p>Nederlandse banken zijn voorlopig niet klaar voor collectieve financiering aan huurders. VrijCoop is een pact tussen huurderscomplexen, waarbij de complexen elkaar kunnen financieren en waarbij juridisch wordt uitgeloten dat de huurwoningen worden omgezet in koopwoningen.</p> <br />

<b>Centraal Wonen: Nieuwe afspraken maken met je verhuurder?</b> <br />
<i>Workshopbegeleider: Peter Bakker - LVCW</i> <br />
<p>Centraal Wonen-projecten hebben problemen met de nieuwe regels die de woningcorporaties moeten toepassen. Kun je dit samen met je verhuurder oplossen? Of moeten CW-projecten hun krachten bundelen en een stevige vuist maken richting de woningcorporaties om Centraal Wonen leefbaar te houden?</p> <br />

<h3>BLOK III : Zelforganisatie en Zelf beheer.</h3> <br />
<h4>Spreker: Hessel Dokkum</h4> <br />
<p>Hessel heeft 30 jaar lang in verschillende collectieve panden gewoond. Hij is daarnaast initiator van verschillende collectieve broedplaatsen en culturele erfpachtpanden. Hij zal spreken over zelfbeheer en zelfwerkzaamheid vanuit zijn ervaring als organisator en verhuurder van panden waarbij hij zijn eigen 30 jarige praktijkervaring als gebruiker van collectieve projecten meeneemt.</p> <br />

<b>Besluitvorming</b> <br />
<i>Workshopbegeleider: Jelmer, Iewan</i>
<p>Een woongemeenschap draait om samen met elkaar te wonen en hierover afspraken te maken. Hoe zorg je dat iedereen mee praat? Welke mogelijkheden zijn er in besluitvorming?
Iewan deelt haar consensus model.</p> <br />

<b>Hoe hou je elkaar betrokken</b> <br />
<i>Workshopbgeleider: Jan Verschure</i> <br />
<p>In het begin gaat alles makkelijk en is iedereen enthousiast. Maar na aantal jaar kan dit veranderen. Er komen nieuwe mensen met een andere instelling. Hoe blijf je betrokken met elkaar en blijf je een echte woongemeenschap? Wat werkt of wat werkt juist niet?</p> <br />

<b>Diversiteit woongroepen</b> <br />
<i>Workshopbegeleider: Kaat, Iewan</i> <br />
<p>Hoe sterk wil je dat de woongemeenschap homogeen is of juist niet. Hoe haal je diversiteit binnen. Zou meer diversiteit een streven moeten zijn?</p> <br />

<b>Individu versus woongroep</b> <br />
<i>Workshopbegeleider: Ani, Iewan</i> <br />
<p>Waar stopt de grens van het individu in de groep? Wanneer moet het individu zich vormen naar de groep? Hoe stel je deze soms ongeschreven regels vast en moet je hierop selecteren bij toelating tot de woongemeenschap?</p> <br />
